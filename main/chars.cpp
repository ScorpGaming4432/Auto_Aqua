void loadCharSet(const uint8_t (*charset)[8], uint8_t count, uint8_t startSlot) { // TEST THIS: for future use (?)
  for (uint8_t i = 0; i < count; i++) {
    lcd->createChar(startSlot + i, charset[i]);
  }
}

