// Default / empty glyphs (used for languages without custom sets)
static const uint8_t emptyGlyphs[8][8] = {
  {0}, {0}, {0}, {0}, {0}, {0}, {0}, {0}
};

// Load up to 8 glyphs for the specified language index into CGRAM slots 0..7.
void loadLanguageGlyphs(uint8_t langIndex) {
  const uint8_t (*set)[8] = emptyGlyphs;

  switch (langIndex) {
    case 0: // Polski
      set = polishGlyphs;
      break;
    case 9: // Čeština
      set = czechGlyphs;
      break;
    case 2: // Русский
      set = russianGlyphs;
      break;
    default:
      set = emptyGlyphs;
      break;
  }

  // Write the 8 glyphs into CGRAM slots 0..7. The LiquidCrystal API expects
  // a non-const pointer for the bitmap parameter, so cast away const.
  loadCharSet((const uint8_t (*)[8])set, 8, 0);
}
